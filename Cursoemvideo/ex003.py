#Crie o programa que leia e some dois números.

n1 = int(input('Digite o primeiro numero : '))
n2 = int(input('Digite o segundo numero : '))
soma = n1 + n2
sub = n1 - n2
print('O resultado da soma de {} com {} é {}'.format(n1, n2, soma))
print('O resultado de {} subtraido de {} é {}'.format(n1, n2, sub))

